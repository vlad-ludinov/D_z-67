package Task2;

public class Vehicle {

    int maxSpeed;
    double coefficientAllowed;

    public Vehicle(int maxSpeed, double coefficientAllowed) {
        this.maxSpeed = maxSpeed;
        this.coefficientAllowed = coefficientAllowed;
    }

    public int getMaxSpeed() {
        return this.maxSpeed;
    }

    public double getCoefficientAllowed() {
        return this.coefficientAllowed;
    }


}
    

